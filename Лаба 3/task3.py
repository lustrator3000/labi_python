def count_letters(str_):
    s = str_.lower()
    dict_ = {}
    for i in range(len(s)):
        if s[i].isalpha():
            if s[i] in dict_:
                dict_[s[i]] += 1
            else:
                dict_[s[i]] = 1 
    return dict_

def calculate_frequency(dictionary: dict):
    sum = 0
    for _ in dictionary: #schetchik
        sum += dictionary[_]
    for elem in dictionary:
        dictionary[elem] = dictionary[elem] / sum
    return dictionary

